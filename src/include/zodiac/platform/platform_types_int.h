/**
 * @file platform_type_int.h
 * Definition of fixed-width integer types.
 *
 * @brief Provides typedefs for fixed-width unsigned and signed integers.
 * These are defined to match the sizes required for 8, 16, 32, and 64-bit integers.
 */

#ifndef ZODIAC_PLATFORM_TYPE_INT_H
#define ZODIAC_PLATFORM_TYPE_INT_H

/**
 * @typedef uint8_t
 * @brief Unsigned 8-bit integer.
 */
typedef unsigned char uint8_t;

/**
 * @typedef uint16_t
 * @brief Unsigned 16-bit integer.
 */
typedef unsigned short uint16_t;

/**
 * @typedef uint32_t
 * @brief Unsigned 32-bit integer.
 */
typedef unsigned int uint32_t;

/**
 * @typedef uint64_t
 * @brief Unsigned 64-bit integer.
 */
typedef unsigned long long uint64_t;

/**
 * @typedef int8_t
 * @brief Signed 8-bit integer.
 */
typedef signed char int8_t;

/**
 * @typedef int16_t
 * @brief Signed 16-bit integer.
 */
typedef signed short int16_t;

/**
 * @typedef int32_t
 * @brief Signed 32-bit integer.
 */
typedef signed int int32_t;

/**
 * @typedef int64_t
 * @brief Signed 64-bit integer.
 */
typedef signed long long int64_t;

#endif // ZODIAC_PLATFORM_TYPE_INT_H
